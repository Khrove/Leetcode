# Sets are used to store multiple items in a single variable

# Sets are unordered, unchangeable, and unindexed
# Set items are unchangeable, but you can remove items and add new ones
# Sets are unordered, meaning it is not possible to know what order items will appear. So set[1] may yield weird results
# Duplicates are not allowed. If a duplicate is detected it will be removed (example below)
# A set can contain one or multiple different data types

# Declaration
set = {"apple", "banana", "cherry", "apple"}
print(set)

# Length of set
print(len(set))

