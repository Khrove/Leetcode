# Tuples are ordered, unchangeable, and allows for duplicate values

# Ordered means that the tuple is forever set to be that order, you cannot add an item after defined

# Unchangeable means that the tuple is unmutable. You cannot change, add, or remove items after definition

# Duplicate values means that items can have the same value

# You must have more than one item in a tuple

# Tuples can be of any data type AND a tuple can contain multiple data types

tuple = ("apple", "banana", "cherry", "apple", "cherry")
tuple2 = ("string", 2, False)
notATuple = ("tuple")
print(tuple)
print(len(tuple))
print(type(notATuple))
